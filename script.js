let currentSection = document.querySelector(".show");

function navigateTo(id) {
    const target = document.getElementById(id);
    if (!target || target === currentSection) return;

    // start hide animation on the currently shown section
    currentSection.classList.add('hide');
    currentSection.setAttribute("aria-hidden", "true");

    // when hide animation finishes, remove classes and reveal target
    const onHidden = () => {
        currentSection.classList.remove('hide', 'show');
        currentSection.removeEventListener('animationend', onHidden);
        currentSection.removeAttribute("aria-current");
        target.classList.add('show');
        target.setAttribute("aria-hidden", "false");
        target.setAttribute("aria-current", "page");
        currentSection = target;

        // Announce section change for screen readers
        const ariaLive = document.createElement("div");
        ariaLive.setAttribute("aria-live", "polite");
        ariaLive.setAttribute("aria-atomic", "true");
        ariaLive.className = "sr-only";
        const sectionName = target.querySelector("h2")?.textContent || id;
        ariaLive.textContent = `Now viewing ${sectionName}`;
        document.body.appendChild(ariaLive);
        setTimeout(() => ariaLive.remove(), 1000);
    };

    currentSection.addEventListener('animationend', onHidden, { once: true });
}

